        /// <summary>
        /// Select the i-th 1-bit in a word. Returns the position of the i-th set bit.
        /// </summary>
        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static int SelectInWord(ulong word, int rank)
        {
            if (rank >= System.Numerics.BitOperations.PopCount(word))
                throw new ArgumentOutOfRangeException(nameof(rank));

            // Use BMI2 if available for fast select
            if (Bmi2.X64.IsSupported)
            {
                var mask = 1UL << rank;
                var one = Bmi2.X64.ParallelBitDeposit(mask, word);
                return System.Numerics.BitOperations.TrailingZeroCount(one);
            }

            // Fallback implementation based on Vigna's broadword select
            const ulong ONES_STEP_4 = 0x1111111111111111UL;
            const ulong ONES_STEP_8 = 0x0101010101010101UL;
            const ulong LAMBDAS_STEP_8 = 0x80UL * ONES_STEP_8;

            var s = word;
            s = s - ((s & (0xAUL * ONES_STEP_4)) >> 1);
            s = (s & (0x3UL * ONES_STEP_4)) + ((s >> 2) & (0x3UL * ONES_STEP_4));
            s = (s + (s >> 4)) & (0xFUL * ONES_STEP_8);
            var byteSums = s * ONES_STEP_8;

            var rankStep8 = (ulong)rank * ONES_STEP_8;
            var geqRankStep8 = ((rankStep8 | LAMBDAS_STEP_8) - byteSums) & LAMBDAS_STEP_8;
            var place = (int)(System.Numerics.BitOperations.PopCount(geqRankStep8) * 8);
            var byteRank = (ulong)rank - (((byteSums << 8) >> place) & 0xFFUL);
            var index = ((word >> place) & 0xFFUL) | (byteRank << 8);
            return place + SELECT_IN_BYTE[index];
        }

